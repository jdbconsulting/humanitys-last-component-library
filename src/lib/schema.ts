// ---------------------------------------------------------------------------
// Vendor family catalog (catalog.json files under hlcl/vendors/<mfg>/<family>/)
// ---------------------------------------------------------------------------

/**
 * The component-kind taxonomy is closed: every vendor family is one of these.
 * Adding a new kind requires a coordinated change in `src/lib/libraries.ts`
 * (which owns the kind labels and ordering shown in the Libraries tab nav).
 */
export const componentKindSchema = z.enum(['resistor', 'capacitor', 'ferrite', 'inductor']);
export type ComponentKind = z.infer<typeof componentKindSchema>;

/**
 * A control declaration on a per-family option. Snake_case on the wire;
 * the runtime FamilyOption shape used in `libraries.ts` (camelCase)
 * is built from this in `libraries.ts::optionFromCatalog`.
 */
export const familyOptionControlCatalogSchema = z.discriminatedUnion('kind', [
	z.object({ kind: z.literal('toggle'), default_value: z.boolean() }),
	z.object({
		kind: z.literal('select'),
		choices: z.array(z.object({ id: z.string(), label: z.string() })).min(1),
		default_value: z.string()
	}),
	z.object({
		kind: z.literal('multiselect'),
		choices: z.array(z.object({ id: z.string(), label: z.string() })).min(1),
		default_value: z.array(z.string())
	}),
	z.object({
		kind: z.literal('number'),
		default_value: z.number(),
		min: z.number().optional(),
		max: z.number().optional(),
		step: z.number().optional(),
		unit: z.string().optional()
	}),
	z.object({
		kind: z.literal('text'),
		default_value: z.string(),
		placeholder: z.string().optional()
	})
]);
export type FamilyOptionControlCatalog = z.infer<typeof familyOptionControlCatalogSchema>;

export const familyOptionCatalogSchema = z.object({
	id: z.string(),
	label: z.string(),
	help: z.string().optional(),
	/** When true, the option offers a "Use global" choice in the family card. */
	overrides_global: z.boolean().optional(),
	control: familyOptionControlCatalogSchema
});
export type FamilyOptionCatalog = z.infer<typeof familyOptionCatalogSchema>;

/**
 * One vendor family catalog entry, sourced from
 * `hlcl/vendors/<manufacturer>/<family>/catalog.json`. The Vite plugin in
 * `vite/plugins/hlcl-build-assets.ts` discovers every such file at
 * dev/build time, validates it against `familyCatalogSchema` below, and
 * emits a merged `src/lib/_generated/catalog.ts` that the UI imports.
 *
 * Adding a new vendor family is therefore a pure data drop: create
 * `hlcl/vendors/<mfg>/<family>/` with a `catalog.json` and the family
 * shows up in the Libraries tab on the next dev save / CI build, with
 * zero TypeScript code changes required.
 */
export const familyCatalogEntrySchema = z.object({
	/** Stable id matching the eventual build target name, e.g. `panasonic-erj`. */
	id: z.string().min(1),
	/** Display name shown in the family card header. */
	name: z.string().min(1),
	/** Manufacturer (Panasonic, Murata, …). */
	manufacturer: z.string().min(1),
	/** Short blurb shown in the family card. */
	summary: z.string().min(1),
	/** Top-level component kind. Drives the "Resistors / Capacitors / …" tabs. */
	kind: componentKindSchema,
	/**
	 * Subtype id within the kind (e.g. `thick-film` under `resistor`). Two
	 * entries that share a `subtype_id` MUST also share a `subtype_label`
	 * — the merger emits a warning otherwise and uses the first label seen.
	 */
	subtype_id: z.string().min(1),
	/** Human-readable subtype label (e.g. "Thick film"). */
	subtype_label: z.string().min(1),
	/**
	 * Sort key controlling the order of subtypes within a kind. Lower
	 * values render first. The merger takes the minimum value seen for
	 * a given subtype across all entries, defaulting to 100.
	 */
	subtype_order: z.number().int().default(100),
	/**
	 * Sort key controlling family order within a subtype. Lower values
	 * render first; ties break alphabetically by id. Defaults to 100 so
	 * vendors that don't care about ordering get a sensible mid-value.
	 */
	sort_order: z.number().int().default(100),
	/**
	 * EIA case sizes this family ships with. Restricts the choices
	 * available in the per-family "Case sizes" multiselect; the order in
	 * the array defines the display order in the UI.
	 */
	available_sizes: z.array(z.string()).min(1),
	/**
	 * Family-specific options BEYOND the common (densities / sizes /
	 * pad-radius) options that every family gets. Use this for things
	 * like Murata GCM's `min_voltage` filter.
	 */
	options: z.array(familyOptionCatalogSchema).default([])
});
export type FamilyCatalogEntry = z.infer<typeof familyCatalogEntrySchema>;

/**
 * The merged catalog: a flat array of validated entries. The UI groups
 * them into the kind / subtype / family tree in `libraries.ts`.
 */
export const familyCatalogSchema = z.array(familyCatalogEntrySchema);
export type FamilyCatalog = z.infer<typeof familyCatalogSchema>;

// ---------------------------------------------------------------------------
// Validation helpers
// ---------------------------------------------------------------------------

export interface ParseResult<T> {
	ok: boolean;
	value?: T;
	errors?: string[];
}

function flattenZodIssues(err: z.ZodError): string[] {
	return err.issues.map((iss) => {
		const path = iss.path.length > 0 ? iss.path.join('.') : '<root>';
		return `${path}: ${iss.message}`;
	});
}

/**
 * Parse + validate a candidate BuildConfig (e.g. from a file the user
 * uploaded, or the result of a `JSON.parse` of imported text). Never
 * throws; returns a discriminated result the caller can render in the UI.
 */
export function parseBuildConfig(raw: unknown): ParseResult<BuildConfig> {
	const result = buildConfigSchema.safeParse(raw);
	if (result.success) return { ok: true, value: result.data };
	return { ok: false, errors: flattenZodIssues(result.error) };
}

/** Parse + validate a localStorage envelope. */
export function parsePersistedState(raw: unknown): ParseResult<PersistedState> {
	const result = persistedStateSchema.safeParse(raw);
	if (result.success) return { ok: true, value: result.data };
	return { ok: false, errors: flattenZodIssues(result.error) };
}

/**
 * Parse + validate a single per-family catalog.json. Used by the Vite
 * plugin (`vite/plugins/hlcl-build-assets.ts`) when discovering vendor
 * folders; pretty-printed errors are surfaced to the dev terminal so a
 * malformed file fails loudly rather than silently being dropped.
 */
export function parseFamilyCatalogEntry(raw: unknown): ParseResult<FamilyCatalogEntry> {
	const result = familyCatalogEntrySchema.safeParse(raw);
	if (result.success) return { ok: true, value: result.data };
	return { ok: false, errors: flattenZodIssues(result.error) };
}

// ---------------------------------------------------------------------------
// Preset files (one preset.json per file under hlcl/presets/)
// ---------------------------------------------------------------------------
//
// The hard-coded `PRESETS` list that used to live in `src/lib/presets.ts`
// has been replaced by per-preset JSON files under `hlcl/presets/`. Each
// file is a small envelope:
//
//   { id, title, tagline, description, bullets[], sort_order, config }
//
// where `config` is a normal `BuildConfig` — the same shape the Generate
// tab emits and the Import button accepts, so a preset file is fully
// round-trippable through the existing import path.
//
// The Vite plugin in `vite/plugins/hlcl-build-assets.ts` discovers every
// such file at dev/build time, validates it against `presetFileSchema`,
// and emits a merged `src/lib/_generated/presets.ts` that the
// PresetsTab imports. Adding or editing a preset is therefore a pure
// data drop: write/edit the JSON, save, and the configurator picks it
// up on the next dev save / production build.

/**
 * One preset, sourced from `hlcl/presets/<id>.json`. Field semantics:
 *
 *   - `id`         — stable identifier used in `localStorage`
 *                    (`ui.active_preset_id`); renaming is breaking.
 *   - `title`      — formatted display name for the preset card header
 *                    (e.g. "Everything · the kitchen sink"). Also seeded
 *                    into `ui.config_name` on apply (after sanitisation).
 *   - `tagline`    — short ALL-CAPS pill shown next to the title
 *                    (e.g. "Cost-optimised").
 *   - `description`— paragraph blurb under the title.
 *   - `bullets`    — green-checkmark bullet list rendered on the card.
 *                    Hand-authored so authors can pick the most
 *                    informative summary for their preset (auto-deriving
 *                    from the BuildConfig diff would lose AEC-Q200-grade,
 *                    HV-creepage style context).
 *   - `sort_order` — integer; lower renders first. Ties break by id.
 *   - `config`     — the full `BuildConfig` to apply when this preset
 *                    is chosen (crosslinks are intentionally preserved
 *                    on apply, see `AppState.applyPreset`).
 */
export const presetFileSchema = z.object({
	id: z.string().min(1),
	title: z.string().min(1),
	tagline: z.string().min(1),
	description: z.string().min(1),
	bullets: z.array(z.string().min(1)).default([]),
	sort_order: z.number().int().default(100),
	config: buildConfigSchema
});
export type PresetFile = z.infer<typeof presetFileSchema>;

/**
 * Parse + validate a single preset.json. Used by the Vite plugin when
 * discovering `hlcl/presets/*.json`; errors surface in the dev
 * terminal so a malformed file fails loudly rather than silently
 * being dropped.
 */
export function parsePresetFile(raw: unknown): ParseResult<PresetFile> {
	const result = presetFileSchema.safeParse(raw);
	if (result.success) return { ok: true, value: result.data };
	return { ok: false, errors: flattenZodIssues(result.error) };
}
